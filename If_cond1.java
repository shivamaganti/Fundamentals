
public class If_cond1 {
	
	public static void main(String args[]) {
		int a = 0;      //Assigning the value;
		
		if(a > 0) {
			System.out.println("Value is positive.");
		}
		
		if(a < 0) {
			System.out.println("Value is Negative.");
		}
		
		if(a == 0) {
			System.out.println("Value is Equal to Zero");
		}
	}

}
